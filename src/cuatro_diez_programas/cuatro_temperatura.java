    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        Thread t = new Thread(new Runnable(){
        public void run(){
        
            String calor= JOptionPane.showInputDialog(null, "Ingresa cuanta temperatura quieres");
            jProgressBar1.setValue(Integer.parseInt(calor));
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(cuatro_temperatura.class.getName()).log(Level.SEVERE, null, ex);
            }
            jLabel5.setText(calor+" °C");
        
        }
        });
        t.start();
        
    }//GEN-LAST:event_jButton3ActionPerformed

