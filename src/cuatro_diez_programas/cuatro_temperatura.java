    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Thread t = new Thread(new Runnable(){
        public void run(){
        
            String calor= JOptionPane.showInputDialog(null, "Ingresa cuanta temperatura quieres de frio");
            if (Integer.parseInt(calor)<=49){
            jProgressBar1.setValue(Integer.parseInt(calor));
            }
            if (Integer.parseInt(calor)>50){
            JOptionPane.showMessageDialog(null, "Activa la temperatura de calor");
            }
            try {
                
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(cuatro_temperatura.class.getName()).log(Level.SEVERE, null, ex);
            }
            jLabel5.setText(calor+" °C");
        
        }
        });
        t.start();
    }//GEN-LAST:event_jButton1ActionPerformed

